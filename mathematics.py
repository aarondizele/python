import math

print("ceil(4.4) = ", math.ceil(4.4))
print("factorial(4) = ", math.factorial(4))
print("trunc(4.4) = ", math.trunc(4.4))
print("pow(2,4) = ", math.pow(4,4))

#Return square root
print("sqrt(4) = ", math.sqrt(4))
print("math.e = {:.6f}".format(math.e))
print("math.pi = {:.6f}".format(math.pi))

#Return e^x
print("exp(4) = {:.2f}".format(math.exp(4)))

#Return log
print("log(20) = {:.2f}".format(math.log(20)))
#Return log 1000, base 10
print("log(1000,10) = {:.2f}".format(math.log(1000,10)))
print("log10(1000) = {:.2f}".format(math.log10(1000)))

#Return degrees
print("degrees(1.5708) = {:.2f}".format(math.degrees(1.5708)))

#Return radians
print("radians(90) = {:.2f}".format(math.radians(90)))

